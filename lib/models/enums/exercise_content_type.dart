/// Enum representing the type of content for an exercise.
///
/// The available content types are:
/// - [image]: Represents an image content.
/// - [video]: Represents a video content.
/// - [gif]: Represents a GIF content.
/// - [none]: Represents no content.
enum ExerciseContentType {
  /// Represents an image content.
  image,

  /// Represents a video content.
  video,

  /// Represents a GIF content.
  gif,

  /// Represents no content.
  none,
}
