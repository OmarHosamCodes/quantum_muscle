/// Enum representing different types of messages.
/// The available types are:
/// - [text]: Represents a text message type.
/// - [image]: Represents an image message type.
/// - [video]: Represents a video message type.
/// - [audio]: Represents an audio message type.
/// - [file]: Represents a file message type.
/// - [request]: Represents a request message type.
/// - [server]: Represents a server message type.
enum MessageType {
  /// Text message type.
  text,

  /// Image message type.
  image,

  /// Video message type.
  video,

  /// Audio message type.
  audio,

  /// File message type.
  file,

  /// Request message type.
  request,

  /// Server message type.
  server,
}
